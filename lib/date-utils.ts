/**
 * ฟังก์ชันแปลงตัวเลขเดือนเป็นชื่อเดือนภาษาไทย
 * @param month - ตัวเลขเดือน (1-12)
 * @returns ชื่อเดือนภาษาไทย หรือค่าว่างถ้าเดือนไม่ถูกต้อง
 */
export const getMonthNameThai = (month: number): string => {
  const monthNames = [
    'มกราคม',
    'กุมภาพันธ์',
    'มีนาคม',
    'เมษายน',
    'พฤษภาคม',
    'มิถุนายน',
    'กรกฎาคม',
    'สิงหาคม',
    'กันยายน',
    'ตุลาคม',
    'พฤศจิกายน',
    'ธันวาคม',
  ];
  return monthNames[month - 1] || '';
};
